# -----------------------------------------------------
# Execute pywal
# -----------------------------------------------------

echo ":: Execute pywal with $used_wallpaper"
wal -i ~/wallpapers/cachyos-wallpapers/ $used_wallpaper -q --cols16

# -----------------------------------------------------
# Reload Waybar
# -----------------------------------------------------

$HOME/.config/waybar/launch.sh

# -----------------------------------------------------
# Pywalfox
# -----------------------------------------------------

if type pywalfox >/dev/null 2>&1; then
  pywalfox update
fi
